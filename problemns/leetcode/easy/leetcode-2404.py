from collections import Counter


def mostFrequentEven(nums):
    nums = [i for i in nums if i % 2 == 0]

    if len(nums) == 0:
        return -1

    counter = Counter(nums)

    maior = -1
    count = 0
    for i in counter.keys():
        if counter.get(i) == 1:
            count += 1
        if counter.get(i) > maior and i % 2 == 0:
            maior = counter.get(i)

    list = []
    for i in counter.keys():
        if counter.get(i) == maior:
            list.append(i)

    if count == len(nums):
        return min(nums)
    return min(list)


def mostFrequentEvenRefactor(nums) -> int:
    freq_list = Counter(nums)

    value_freq = -1
    value = -1

    for i in freq_list:
        if i % 2 == 0:
            if freq_list[i] > value_freq:
                value_freq = freq_list[i]
                value = i
            if freq_list[i] == value_freq:
                if i < value:
                    value = i

    return value


